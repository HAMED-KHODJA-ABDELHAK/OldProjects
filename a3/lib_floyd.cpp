void floyd::lib_read_adjacency(std::istream& in, int **matrix) {
	int nodes;
	in >> nodes;

	matrix = new int[nodes][nodes];

	for (int i = 0; i < nodes; ++i)
		for (int j = 0; j < nodes; ++j)
			in >> matrix[i][j];
}

